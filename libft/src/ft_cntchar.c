#include "libft.h"

size_t	ft_cntchar(const char *s, char c)
{
	size_t		n;

	n = 0;
	while (1)
	{
		if (*s != c && *s)
		{
			n++;
			while (*s != c && *s)
				s++;
		}
		if (*s == '\0')
			break ;
		s++;
	}
	return (n);
}
